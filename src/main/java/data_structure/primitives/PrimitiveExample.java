package data_structure.primitives;

public class PrimitiveExample {

    public static void main(String[] args) {
        // 기본 자료형 변수 선언
        int a = 1;
        double b = 1.1;
        char c = 'a';
        boolean d = true;

        // 기본 자료형 변수 출력
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
